CREATE TABLE IF NOT EXISTS EMPLOYEE (
    EMPLOYEE_ID integer generated by default as identity (start with 10000) NOT NULL,
    FIRST_NAME text NOT NULL,
    MIDDLE_NAME text,
    LAST_NAME text NOT NULL,
    PREFERRED_NAME text,
	DATE_OF_BIRTH date NOT NULL,
    EMAIL_ADDRESS text,
    ADDRESS_LINE1 text NOT NULL,
    ADDRESS_LINE2 text,
    ZIP_CODE text,
    PHONE_NUMBER float8,
	AUDIO_FILE_PATH text,
	FILE_EXTENSION text,
	AUDIO_CONTENT BYTEA,
    CONSTRAINT PK_EMPLOYEE_ID PRIMARY KEY (EMPLOYEE_ID hash)
	);
